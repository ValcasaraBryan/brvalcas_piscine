int		ft_max(int *tab, int length)
{
	int i;
	int comp;

	i = 0;
	comp = tab[0];
	while (length > 0)
	{
		if (tab[i] > comp)
			comp = tab[i];
		i++;
		length--;
	}
	return (comp);
}

char		*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	swap;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	i--;
	while (j < i)
	{
		swap = str[i];
		str[i] = str[j];
		str[j] = swap;
		j++;
		i--;
	}
	return (str);
}

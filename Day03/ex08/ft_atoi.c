int		ft_atoi(char *str)
{
	int res;
	int neg;

	res = 0;
	neg = 1;
	while ((*str <= 9 && *str >= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
	{
		neg = (*str == '-' ? -1 : 1);
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + *str - 48;
		str++;
	}
	return (res * neg);
}
